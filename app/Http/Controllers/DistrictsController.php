class DistrictsController extends Controller
{
    public function migrateAccountingId(Request $request)
    {
        $userData = $request->users_data;
        if (empty($userData) || (int)$userData->role_id !== 999999999) {
            return response()->json(['data' => ['message' => 'Permission denied !']]);
        }

        $districts = District::query()->get();
        if (empty($districts)) {
            return response()->json(['data' => ['message' => 'Empty districts']]);
        }

        $provinces = u::convertArrayToObject(Province::query()->get(), 'id');
        $success = 0;
        $error = 0;

        foreach ($districts as $district) {
            try {
                $province = $provinces[$district->province_id];
                foreach (DISTRICTS as $key => $value) {
                    $pOfDistrict = substr($key, 0, 2);
                    $fValue = strtolower(str_replace(' ', '', u::utf8convert($value)));
                    $dName = strtolower(str_replace(' ', '', u::utf8convert($district->name)));

                    if ($pOfDistrict === $province->accounting_id && $fValue === $dName) {
                        $district->accounting_id = $key;
                        if ($district->save()) {
                            ++$success;
                        } else {
                            ++$error;
                        }
                    }
                }
            } catch (\Exception $e) {
                $error++;
            }
        }

        return response()->json(['data' => ['message' => 'Success', 'success' => $success, 'error' => $error]]);
    }

    public function getList(Request $request, District $model, Response $response){
        $params = $request->all();
        $data = $model->getList($params);
        return $response->formatResponse(APICode::SUCCESS, ['data' => $data]);
    }
}

